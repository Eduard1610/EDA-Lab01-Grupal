public class JavaBooleans {
  public static void main(String [] args) {
    /*
    VALORES BOOLEANOS
    Son un tipo de variable que solo adquiere 2 valores: TRUE รณ FALSE
    Para su declaracion se usa la keyword "boolean"
    */
    boolean isEDAFun = true;
    boolean likeBroccoli = false;
    /*
    EXPRESIONES BOOLEANAS
    Expresiones que devuelven un valor booleano
    Hacen uso de operadores tales como: > , < , ==
    */
    int x = 10, y = 15, z = 10;
    System.out.println(x + " > " + y + "? " + (x > y));
    System.out.println(x + " < " + y + "? " + (x > y));
    System.out.println(x + " == " + z + "? " + (x == z));
  }
}
